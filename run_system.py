async def run_api_server():
    """Ğ—Ğ°Ğ¿ÑƒÑĞº API ÑĞµÑ€Ğ²ĞµÑ€Ğ°"""
    import uvicorn
    from api.main import app
    
    config = uvicorn.Config(
        app,
        host="0.0.0.0",
        port=8000,
        log_level="info",
        access_log=True
    )
    
    server = uvicorn.Server(config)
    await server.serve()


